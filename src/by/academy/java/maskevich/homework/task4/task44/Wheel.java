package by.academy.java.maskevich.homework.task4.task44;
public class Wheel {
    private double radius;
    private String pos;
    private final double RADIUS = 15;
    Wheel() {
        this.radius = RADIUS;
    }
    Wheel(double radius) {
        this.pos = pos;
        this.radius = radius;
    }
    public String getPos() {
        return pos;
    }
    public double getRadius() {
        return radius;
    }
}